"""
Модуль с исключениями для системы плагинов Aegnux.

Определяет иерархию исключений, которые могут возникать
при работе с плагинами. Все исключения наследуются от базового
класса PluginError.

Пример использования:
    try:
        manager.load_plugin("my_plugin")
    except PluginNotFoundError:
        logging.error("Плагин не найден")
    except PluginLoadError as e:
        logging.error(f"Ошибка загрузки плагина: {e}")
"""


class PluginError(Exception):
    """
    Базовое исключение для всех ошибок, связанных с плагинами.
    
    От этого класса наследуются все остальные исключения плагинов,
    что позволяет перехватывать все ошибки плагинов одним блоком except.
    """
    pass


class PluginLoadError(PluginError):
    """
    Исключение, возникающее при ошибках загрузки плагина.
    
    Возможные причины:
    - Ошибка импорта модуля
    - Отсутствие необходимых зависимостей
    - Синтаксические ошибки в коде плагина
    """
    pass


class PluginNotFoundError(PluginError):
    """
    Исключение, возникающее когда плагин не найден.
    
    Возможные причины:
    - Неверное имя плагина
    - Отсутствие файла plugin.py
    - Плагин не установлен
    """
    pass


class PluginInitError(PluginError):
    """
    Исключение, возникающее при ошибках инициализации плагина.
    
    Возможные причины:
    - Ошибки в методе initialize()
    - Отсутствие необходимых ресурсов
    - Конфликты с другими плагинами
    """
    pass


class PluginDependencyError(PluginError):
    """
    Исключение, возникающее при проблемах с зависимостями плагина.
    
    Возможные причины:
    - Отсутствие необходимого плагина
    - Конфликт версий
    - Циклические зависимости
    """
    pass


class PluginRuntimeError(PluginError):
    """
    Исключение, возникающее во время работы плагина.
    
    Возможные причины:
    - Ошибки в обработчиках событий
    - Проблемы с доступом к ресурсам
    - Внутренние ошибки плагина
    """
    pass
